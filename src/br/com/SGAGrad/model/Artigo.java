package br.com.SGAGrad.model;

public class Artigo {

	private String nome;
    private String qualis;
    
    public Artigo(){
    
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getQualis() {
        return qualis;
    }

    public void setQualis(String qualis) {
        this.qualis = qualis;
    }
    
}
